export function basescanAddressUrl(address: string) {
  return `https://basescan.org/address/${address}`;
}

export function basescanTxUrl(txHash: string) {
  return `https://basescan.org/tx/${txHash}`;
}

export function blockscoutAddressUrl(address: string) {
  return `https://base.blockscout.com/address/${address}`;
}

export function blockscoutTxUrl(txHash: string) {
  return `https://base.blockscout.com/tx/${txHash}`;
}

